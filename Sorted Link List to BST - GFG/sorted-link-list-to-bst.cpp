class Solution{
  public:
    TNode* construct(int low, int high, vector<int>& arr){
        if(low > high)
            return NULL;
        int mid = ceil((float) (low+high)/2);
        //cout<<mid<<" ";
        TNode* root = new TNode(arr[mid]);
        root->left = construct(low,mid-1,arr);
        root->right = construct(mid+1,high,arr);
        return root;
    }
    TNode* sortedListToBST(LNode *head) {
        //code here
        vector<int> arr;
        while(head){
            arr.push_back(head->data);
            head=head->next;
        }
        return construct(0,arr.size()-1,arr);
    }
};

