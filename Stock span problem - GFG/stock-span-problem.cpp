class Solution
{
    public:
    //Function to calculate the span of stockâ€™s price for all n days.
    vector <int> calculateSpan(int price[], int n)
    {
       stack<int> st;
       vector<int> result(n);
       for(int i = 0; i < n; i++)
       {
           int ans = 1;
           while(!st.empty() && price[st.top()] <= price[i])
           {
               ans += result[st.top()];
               st.pop();
           }
           st.push(i);
           result[i] = ans;
       }
       return result;
    }
};



